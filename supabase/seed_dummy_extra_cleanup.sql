-- حذف كل البيانات الوهمية التي أضافها seed_dummy_extra.sql (شغّله وقتما تنتهي من التجربة).
-- الترتيب مهم: الوجبات أولاً (تحذف عناصرها تلقائياً)، ثم المنتجات (تحذف خياراتها تلقائياً)، ثم الأصناف إن أصبحت فارغة.

-- 1) الوجبات (Combos) — يحذف combo_items تلقائياً
delete from combos where name in ('وجبة كلاسيك', 'وجبة دبل تشيز');

-- 2) المنتجات — يحذف modifier_groups/modifiers تابعتها تلقائياً
delete from products where name in (
  'برجر لحم كلاسيك',
  'برجر دجاج مقرمش',
  'برجر دبل تشيز',
  'بطاطس مقلية',
  'بطاطس بالجبن والصلصة',
  'صوص باربكيو',
  'صوص رانش',
  'صوص ثوم',
  'بيبسي',
  'سفن أب',
  'عصير برتقال طازج',
  'ماء معدني'
);

-- 3) الأصناف — تُحذف فقط إذا لم يعد بها أي منتج (تحسباً لو أضفت منتجات حقيقية بنفس الصنف)
delete from categories c
where c.name in ('برجر', 'اضافات', 'مشروبات')
  and not exists (select 1 from products p where p.category_id = c.id);
